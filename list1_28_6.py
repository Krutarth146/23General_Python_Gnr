# Slicing

lst3 = [10,20,30,40,50,50,60,70,80,80,60,40,90]

# print(lst3[start : end(n-1): step(n-1)])

print(lst3[1:6])  # [20,30,40,50,50]
print(lst3[6:1])  # []
print(lst3[5:6])  # [50]
print(type(lst3[5:6]))  # <class 'list'>
print(lst3[:])  # [10, 20, 30, 40, 50, 50, 60, 70, 80, 80, 60, 40, 90]
print(lst3[5:5])  # []
print(lst3[5:])  # [50, 60, 70, 80, 80, 60, 40, 90]
print(lst3[5:-1])  # [50, 60, 70, 80, 80, 60, 40]
print(lst3[-1:5])  # []
print(lst3[:4])  # [10, 20, 30, 40]
print(lst3[:-2])  # [10, 20, 30, 40, 50, 50, 60, 70, 80, 80, 60]
print(lst3[-2:])  # [40, 90]
print(lst3[1:-6])  # [20, 30, 40, 50, 50, 60]
print(lst3[-9:0])  # []
print(lst3[-2:-7])  # []

lst3 = [10,20,30,40,50,50,60,70,80,80,60,40,90]

print(lst3[3::2])  # [40, 50, 70, 80, 40]
print(lst3[::1])  # [10, 20, 30, 40, 50, 50, 60, 70, 80, 80, 60, 40, 90]
print(lst3[::-1])  # [90, 40, 60, 80, 80, 70, 60, 50, 50, 40, 30, 20, 10]
print(lst3[:0:-1])  # [90, 40, 60, 80, 80, 70, 60, 50, 50, 40, 30, 20]
print(lst3[-5:-10:-3])  # [80,50]
print(lst3[-3:4:2])  # []
print(lst3[-5::])  # [80, 80, 60, 40, 90]
print(lst3[10:-2:-3])  # []
print(lst3[5:-7:-1])  # []
print(lst3[5:-7:1])  # [50]